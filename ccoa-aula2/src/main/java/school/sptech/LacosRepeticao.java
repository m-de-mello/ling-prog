package school.sptech;

public class LacosRepeticao {
    public static void main(String[] args) {
        // FOR
        for (int i = 0; i <= 10; i++) {
            System.out.println(i);
        }

        // WHILE
        Integer contador = 0;

        while (contador < 10) {
            System.out.println("Contador:" + contador);
            contador++;
        }
    }
}
